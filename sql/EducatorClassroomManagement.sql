-- ==================== CREATE SUBJECTS TABLE ====================
CREATE TABLE IF NOT EXISTS public.tbl_subjects (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  educator_id BIGINT NOT NULL REFERENCES public.tbl_users(id) ON DELETE CASCADE,
  sections_id BIGINT NOT NULL REFERENCES public.tbl_sections(id) ON DELETE CASCADE,
  subject_code TEXT NOT NULL,
  subject_name TEXT NOT NULL,
  is_active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT tbl_subjects_unique_code_per_section UNIQUE (educator_id, sections_id, subject_code),
  CONSTRAINT tbl_subjects_unique_name_per_section UNIQUE (educator_id, sections_id, subject_name)
);

CREATE INDEX IF NOT EXISTS idx_tbl_subjects_educator_id ON public.tbl_subjects(educator_id);
CREATE INDEX IF NOT EXISTS idx_tbl_subjects_sections_id ON public.tbl_subjects(sections_id);
CREATE INDEX IF NOT EXISTS idx_tbl_subjects_subject_code ON public.tbl_subjects(subject_code);
CREATE INDEX IF NOT EXISTS idx_tbl_subjects_subject_name ON public.tbl_subjects(subject_name);

-- ==================== ENABLE RLS ON SUBJECTS TABLE ====================
ALTER TABLE public.tbl_subjects ENABLE ROW LEVEL SECURITY;

